    private void setEditableFields(boolean editable) {
               
            jTextField4.setEditable(editable); // name
            jTextField1.setEditable(editable); // username
            jTextField6.setEditable(editable); // password 
            
            if (editable) {
                jTextField6.setText(fullPassword);  // show real
            } else {
                jTextField6.setText(maskedPassword); // show masked
            }

            Color bg = editable ? Color.WHITE : Color.decode("#f5f5f5");
            jTextField4.setBackground(bg);
            jTextField1.setBackground(bg);
            jTextField6.setBackground(bg);
        }
    
     
