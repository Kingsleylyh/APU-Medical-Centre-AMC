    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        jButton1.setBackground(Color.decode("#1a395f"));
        
        if (!editing) {
            editing = true;
            setEditableFields(true);
            jButton1.setText("Save");
            return;
        }

        String newUsername = jTextField1.getText().trim();
        String newName     = jTextField4.getText().trim();
        String newPassword = jTextField6.getText().trim(); 

        if (newUsername.isEmpty() || newName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username and Name cannot be empty.");
            return;
        }

        customer.setUsername(newUsername);
        customer.setName(newName);
        
        customer.setPassword(newPassword);
        fullPassword = newPassword;
        maskedPassword = newPassword.isBlank() ? "" : "*".repeat(newPassword.length());

        boolean ok = CustFileHandler.updateCustomer(customer);
        if (!ok) {
            JOptionPane.showMessageDialog(this, "Failed to save profile.");
            return;
        }

        JOptionPane.showMessageDialog(this, "Profile updated.");

        editing = false;
        setEditableFields(false);
        jButton1.setText("Edit Profile");
    }//GEN-LAST:event_jButton1ActionPerformed

